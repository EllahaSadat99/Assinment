package education;

public class SwitchExampales {

	public static void main(String[] args) {
		int number = 4;
		switch (number) {
		case 1:
			System.out.println("It is Number five");
			break;
		case 2:
			System.out.println("It is Number six");
			break;
		case 3: 
			System.out.println("It is Number seven");
			default: 
				System.out.println("It is Number ten");
				break; 
		}
	
		
		
	
			
		
		
		// Switch
		
		int day = 2; 
		switch (day) {
		    
		case 1: 
			System.out.println("It is Monday");
			break;
		case 2: 
			System.out.println("It is Tusday");
			break; 
		case 3: 
			System.out.println("It is Wednesday");
			break; 
		case 4:
			System.out.println("It is Thursday");
			break; 
		case 5:
			System.out.println("It is Friday");
			break; 
		default:
			System.out.println("It is Weekend");
			break; 
			

		}
		

	}

}
