
public class forLoop {

	public static void main(String[] args) {
		// 
		
		// i <= 5
		// i++
		for ( int i =1; i<= 5; i++) {
			System.out.println(i);
		}
		for (int  i = 1; i <= 5; i++);
		System.out.println("Hello World");
		
		for (int i = 5; i >= 1; i--) {
			System.out.println(i);
		}

	}

}
