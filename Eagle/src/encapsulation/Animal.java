public class Animal {
	
	private String name ; 
	private int wight  ;
	private int legs;
	
	public String getName () {
		return name ; 
	}
	public void setName (String animalName ) {
		name = animalName  ;
	}
	public int getWight ( ) {
		return wight ; 
	}
	public void setWight (int animalWight ) {
		wight = animalWight;
	}
	public int getLegs ( ) {
		return legs;
	}
	public void setLegs (int animalLegs ) {
		legs = animalLegs;
		
		
		
		
		
	
	  



	}

}
